a=2;
t=[-5:0.1:5];
x=exp(-t/a);

figure;
plot(t,x,'red')
xlabel('t')
ylabel('x')
legend({'x(t)'})
figure;
plot(-t,x,'blue')
xlabel('t')
ylabel('x')
legend({'x(-t)'})
figure;
plot(t+1.5*a,x,'green')
xlabel('t')
ylabel('x')
legend({'x(t-1.5\alpha)'})
figure;
plot(t/4,x,'black')
xlabel('t')
ylabel('x')
legend({'x(2\alphat)'})